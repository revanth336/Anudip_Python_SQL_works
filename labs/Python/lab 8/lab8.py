import numpy as np

# Array of 10 zeros
array_zeros = np.zeros(10)
print("An array of 10 zeros:")
print(array_zeros)
print()

# Array of 10 ones
array_ones = np.ones(10)
print("An array of 10 ones:")
print(array_ones)
print()

# Array of 10 fives
array_fives = np.ones(10) * 5
print("An array of 10 fives:")
print(array_fives)
